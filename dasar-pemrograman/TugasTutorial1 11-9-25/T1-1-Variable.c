#include <stdio.h>

int main(){
    int angka=10;
    float bilangan=3.14;
    char karakter='A';
    double desimal=2.718282;
    printf("Nilai integer: %d\n", angka);
    printf("Nilai float: %.2f\n", bilangan);
    printf("Nilai char: %c\n", karakter);
    printf("Nilai double: %.6lf\n", desimal);
    return 0;
}